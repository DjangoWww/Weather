import UIKit

extension Int {

    /*
      const
     */
//    public static let zero: Int = 0

    /*
      mathematic
     */
    public var half: Int {
        return self / 2
    }

    public var double: Int {
        return self * 2
    }

    /*
     value  conversion
     */
    public var floatValue: Float {
        return Float(self)
    }

    public var CGFloatValue: CGFloat {
        return CGFloat(self)
    }

    public var doubleValue: Double {
        return Double(self)
    }

    public var stringValue: String {
        return String(self)
    }

    public var int32Value: Int32 {
        return Int32(self)
    }

    public var absValue: Int32 {
        return abs(int32Value)
    }

    public var uintValue: UInt {
        return UInt(self)
    }
}

